public class Preco {
    private int id;
    private float valor;
    private int nivelUsu;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }

    public int getNivelUsu() {
        return nivelUsu;
    }

    public void setNivelUsu(int nivelUsu) {
        this.nivelUsu = nivelUsu;
    }
    
}
